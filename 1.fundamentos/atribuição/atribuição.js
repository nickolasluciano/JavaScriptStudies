const a = 7
let b = 3

b+= a // atribuição adtiva (b = b + a)
console.log(b)

b-= 4 // atribuição subtrativa (b = b - 4)
console.log(b)

b*= 2 // atribuição multiplicativa (b = b * 2)
console.log(b)

b /= 2 // atribuição divisiva (b = b / 2)
console.log(b)

b %= 2 //atribuição modular (b = b%2 ) resto da divisão
console.log(b)
